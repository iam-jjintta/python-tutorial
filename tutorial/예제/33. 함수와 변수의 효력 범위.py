
# 함수와 변수의 효력 범위 확인하기
a = 1
def f(a):
    a = a +1

# 실행하면 결과가 2가 아닌, 1이 나온다.
# 그 이유는 함수에 정의된 매개변수와 함수 밖에 정의된 변수는 별개이므로, 상관없기 때문이다.
f(a)
print(a)

# 새로운 함수 f2 정의
def f2(b):
    b = b + 1

f(3)
# 실행하면 변수 'b'가 없으므로, 에러가 난다.
# 변수 'b'는 함수 f2에만 존재하는 변수이다.
print(b)

