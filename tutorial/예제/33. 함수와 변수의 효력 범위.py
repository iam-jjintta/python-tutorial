# 2. global 키워드 사용하기
# global 키워드는 함수 밖의 변수를 함수 내에서도 사용할 수 있도록 해주는 키워드이다.
# 그러나 프로그래밍을 할 때 global 키워드는 사용하지 않는 것이 좋다고 한다.
d = 1
def f4():
    global d
    d = d + 1

f4()
print(d)
