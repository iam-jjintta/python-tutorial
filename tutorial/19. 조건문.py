
a = 1
b = 1

# 참일 경우
if a == b:
    print('a is same to b.')
# 거짓일 경우
else:
    print('a is not same to b.')

# 거짓일 경우
if a != b:
    print('a is not same to b.')
else:
    print('a is same to b.')

c = 3

# a가 c보다 클 경우
if a > c:
    print('a is bigger than c.')
# a가 c보다 작을 경우
elif a < c:
    print('a is smaller than c')
# a가 c와 같을 경우
else:
    print('a is same to c.')
